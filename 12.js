 //Booleans (true,false) and comparision operator (==,<,>,<=,>=, etc.)

let n1 = 5;
let n2 = 7;

console.log(n1>n2);
console.log(n1<=n2);

 // == vs ===

 n1 = 5;
 n2 = "5";

 console.log(n1==n2);  // It compares only value
 console.log(n1===n2);  // It compares value and data type both simultanously

 // != vs !==

 console.log(n1!=n2);  // It compares only value
 console.log(n1!==n2);  // It compares value and data type both simultanously